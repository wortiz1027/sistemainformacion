package co.com.sistema.sistemainformacion.domain;

public class CaracterEntidad {
    
    private int idCaracter;
    private String nombreCaracter;

    public CaracterEntidad() {
    }

    public int getIdCaracter() {
        return idCaracter;
    }

    public void setIdCaracter(int idCaracter) {
        this.idCaracter = idCaracter;
    }

    public String getNombreCaracter() {
        return nombreCaracter;
    }

    public void setNombreCaracter(String nombreCaracter) {
        this.nombreCaracter = nombreCaracter;
    }
    
    
    
}