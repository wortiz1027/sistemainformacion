package co.com.sistema.sistemainformacion.domain;

public class OtrosEstudios {
    
    private int idOtros;
    private String nombreEstudios;

    public OtrosEstudios() {
    }

    public int getIdOtros() {
        return idOtros;
    }

    public void setIdOtros(int idOtros) {
        this.idOtros = idOtros;
    }

    public String getNombreEstudios() {
        return nombreEstudios;
    }

    public void setNombreEstudios(String nombreEstudios) {
        this.nombreEstudios = nombreEstudios;
    }
    
    
    
}
